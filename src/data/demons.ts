// Группировка по буквам
export function groupByLetter(list: Demon[]): Record<string, Demon[]> {
  return list.reduce((acc, demon) => {
    const l = demon.letter;
    if (!acc[l]) acc[l] = [];
    acc[l].push(demon);
    return acc;
  }, {} as Record<string, Demon[]>);
}

// Уникальные буквы в алфавитном порядке
export function getLetters(list: Demon[]): string[] {
  return [...new Set(list.map(d => d.letter))].sort((a, b) =>
    a.localeCompare(b, 'ru')
  );
}
